def mostrar_suma(num1: int, num2: int):
    suma = num1 + num2
    return suma

def mostrar_resta(num1: int, num2: int):
    resta = num1 - num2
    return resta

def mostrar_multiplicacion(num1: int, num2: int):
    multiplicacion = num1 * num2
    return multiplicacion

def mostrar_division(num1: int, num2: int):
    division = num1 * num2
    return division